// Intro switch case

const ticket = "2023NT3IRA3";
let message = '';

switch(ticket){
    case '2023ME14':
        message = 'Meia: Dirija-se a até a fila A';
        break;
        case '2023NT3IRA':
            message = 'Inteira: DIrija-se até a fila B';
            break;
            default:
                message = 'Código inválido';
                break;
}